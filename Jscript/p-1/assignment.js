// Assignment Operation Demonstration

// Initial value assignment using arithmetic operation
let val = 21 + 43; // val is now 64

// Declaring a variable num1 and assigning it a value
let num1 = 21;

// Modifying num1 using a shorthand assignment operator
// Equivalent to: num1 = num1 / 5;
num1 /= 5; // num1 is now 4.2

// Output the modified value of num1 to the console
console.log(num1); // Outputs: 4.2
 