// Operators in JavaScript

// ADDITION
let addition = 4 + 6; // Adds 4 and 6
console.log(addition); // Outputs: 10

// SUBTRACTION
let subtraction = 9 - 2; // Subtracts 2 from 9
console.log(subtraction); // Outputs: 7

// MULTIPLICATION
let mult = 5 * 4; // Multiplies 5 by 4
console.log(mult); // Outputs: 20

// DIVISION
let div = 9 / 2; // Divides 9 by 2
console.log(div); // Outputs: 4.5

// EXPONENTIAL
let expo = 4 ** 4; // Raises 4 to the power of 4
console.log(expo); // Outputs: 256

// REMAINDER
let rem = 9 % 2; // Finds the remainder of 9 divided by 2
console.log(rem); // Outputs: 1

// Increment
let score = 21; // Initializes score variable
score++; // Increments score by 1
console.log(score); // Outputs: 22

// Decrement
let yourscore = 55; // Initializes yourscore variable
yourscore--; // Decrements yourscore by 1
console.log(yourscore); // Outputs: 54

// Comparison Operations
let num1 = 3; // Initializes num1
let num2 = 4; // Initializes num2
let num3 = 90; // Initializes num3

console.log(num2 == num1); // Checks if num2 is equal to num1 (False)
console.log(num3 == num2); // Checks if num3 is equal to num2 (False)
console.log(num3 > num2); // Checks if num3 is greater than num2 (True)
console.log(num1 < num3); // Checks if num1 is less than num3 (True)
