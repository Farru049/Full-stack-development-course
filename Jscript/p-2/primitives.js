// String Interpolation Example

// Declaring a string variable
let stringOne = 'Farhaan Ali..!';

// Using template literals for string interpolation
let twoString = `Hello, this is ${stringOne}`;
// Output the interpolated string to the console
console.log(twoString); // Outputs: Hello, this is Farhaan Ali..!

// Symbol Example
let sym1 = Symbol(); // Creating a unique Symbol
let sym2 = Symbol(); // Creating another unique Symbol

// Comparing two Symbols
console.log(sym1 == sym2); // Outputs: false (Symbols are unique)

